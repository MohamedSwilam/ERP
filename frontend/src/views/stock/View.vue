<script>
import Ripple from 'vue-ripple-directive'
import ToastificationContent from '@core/components/toastification/ToastificationContent.vue'

export default {
  name: 'ViewStock',
  directives: {
    Ripple,
  },
  data: () => ({
    stocks: {
      isLoading: false,
      isLoadingDelete: false,
      data: null,
    },
  }),
  mounted() {
    this.viewStock()
  },
  methods: {
    viewStock() {
      this.stocks.isLoading = true
      this.$store.dispatch('stock/view', this.$route.params.id).then(response => {
        this.stocks.data = response.data.data
        this.stocks.isLoading = false
      }).catch(error => {
        console.error(error)
        this.stocks.isLoading = false
      })
    },
    deleteStock(id) {
      this.$bvModal
        .msgBoxConfirm('You will not be able to retrieve this again!', {
          title: 'Are you sure?',
          size: 'sm',
          okTitle: 'Yes, Delete',
          okVariant: 'danger',
          cancelTitle: 'Cancel',
          cancelVariant: 'outline-primary',
          centered: true,
        })
        .then(confirmed => {
          if (confirmed) {
            this.stocks.isLoadingDelete = true
            this.$store.dispatch('stock/delete', id).then(response => {
              this.stocks.isLoadingDelete = false
              this.$toast({
                component: ToastificationContent,
                props: {
                  title: 'Success',
                  icon: 'CheckCircleIcon',
                  text: response.data.message,
                  variant: 'success',
                },
              },
              {
                position: 'bottom-right',
                timeout: 5000,
              })
              this.$router.push('/stocks')
            }).catch(error => {
              console.error(error)
              this.stocks.isLoadingDelete = false
            })
          }
        })
    },
  },
}
</script>

